var day = Number(prompt("Enter tha day "));
switch (day) {
    case 0:
        document.write("Today is Monday");
        break;
    case 1:
        document.write("Today is Tusday");
        break;
    case 2:
        document.write("Today is wensday");
        break;
    case 3:
        document.write("Today is Thusday");
        break;
    case 4:
        document.write("Today is Friday");
        break;
    case 5:
        document.write("Today is Saterday");
        break;
    case 6:
        document.write("Today is  sunday");
        break;
    default:
        document.write("this is not day");

}